package com.example.TestVer.Airport.AirVO;

public class AirportVO {

    private String KORname;
    private String airportCode;
    private String KorCountryName;
    private String EncityName;


    public String getKORname() {
        return KORname;
    }

    public void setKORname(String KORname) {
        this.KORname = KORname;
    }

    public String getAirportCode() {
        return airportCode;
    }

    public void setAirportCode(String airportCode) {
        this.airportCode = airportCode;
    }

    public String getKorCountryName() {
        return KorCountryName;
    }

    public void setKorCountryName(String korCountryName) {
        KorCountryName = korCountryName;
    }

    public String getEncityName() {
        return EncityName;
    }

    public void setEncityName(String encityName) {
        EncityName = encityName;
    }
}
